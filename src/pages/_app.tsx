// let Test = '`:root {body {background-color: blue;color: #121212;}}[data-theme="dark"] {body {background-color: red;color: #fff;}}`'

function App({ Component, pageProps }: AppProps) {

    

    const { theme, setTheme } = useTheme()

    const getUserSetPreference = () => {return localStorage.getItem("theme");};
    const storeUserSetPreference = (pref:any) => {localStorage.setItem("theme", pref);};

    // const [theme, setTheme] = useState("light");

    const themeToggler = () => {
        storeUserSetPreference(theme === "light" ? "dark" : "light");
        theme === "light" ? setTheme("dark") : setTheme("light");
    };


    useEffect(() => {
        const userSetPreference = getUserSetPreference();
        if (userSetPreference) {
            setTheme(userSetPreference)
        }
    }, [theme])
    
    return (
        <Provider store={store}>
            {/* <ThemeProvider theme={ theme === 'light' ? lightTheme : darkTheme }> */}
            <ThemeProvider>
            {/* <GlobalStyles styles={Test}/> */}
                <Head>
                    <title>Frederick Go Portfolio</title>
                    <meta name="description" content="Generated by create next app" />
                    <meta name="viewport" content="width=device-width, initial-scale=1" />
                    <link rel="icon" href="/favicon.ico" />
                </Head>
                <Header themeToggler={themeToggler}></Header>
                <main className={styles.main}>
                    <Component {...pageProps} />
                </main> 
                <Footer></Footer>
            </ThemeProvider>
        </Provider>
    )
}

export default App